/**
 * Retorna a região administrativa correta baseada no CEP
 */
export function getDFRegionByCEP(cep: string): string | null {
  // Remover formatação do CEP
  const cleanCEP = cep.replace(/\D/g, '');

  if (cleanCEP.length !== 8) {
    return null;
  }

  // Tentar encontrar correspondência exata primeiro (5 dígitos)
  const fiveDigit = cleanCEP.substring(0, 5);
  if (DF_REGIONS[fiveDigit]) {
    return DF_REGIONS[fiveDigit];
  }

  // Tentar correspondência com 4 dígitos
  const fourDigit = cleanCEP.substring(0, 4);
  if (DF_REGIONS[fourDigit]) {
    return DF_REGIONS[fourDigit];
  }

  // Tentar correspondência com 3 dígitos
  const threeDigit = cleanCEP.substring(0, 3);
  if (DF_REGIONS[threeDigit]) {
    return DF_REGIONS[threeDigit];
  }

  // Se não encontrar, retornar null (usará o retorno da ViaCEP)
  return null;
}

/**
 * Verifica se um CEP é do Distrito Federal
 */
export function isDFCEP(cep: string): boolean {
  const cleanCEP = cep.replace(/\D/g, '');

  if (cleanCEP.length !== 8) {
    return false;
  }

  // CEPs do DF começam com 70, 71, 72 ou 73
  const prefix = cleanCEP.substring(0, 2);
  return ['70', '71', '72', '73'].includes(prefix);
}